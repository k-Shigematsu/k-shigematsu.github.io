<h3>クッキー等について</h3>
<p>本サービスでは、クッキー（Cookie）と呼ばれる技術を利用しています。<br/>
<br/>
クッキーとは、特定のテキスト情報をお客様の利用する通信機器に一時的にデータとして保持させ、接続の度にそのデータを基にお客様を識別させる仕組みをいいます。 当班では、クッキーの利用を前提としたサービスを提供しているため、クッキーの利用を許可しない場合、当班の一部のサービスを受けられないことがあります。<br/>
<br/>
